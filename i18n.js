// Active lang — populated at runtime
let _lang = 'de';

function setLang(code) {
  _lang = (code && I18N[code]) ? code : 'de';
}

function t(key) {
  return (I18N[_lang] && I18N[_lang][key]) || I18N['de'][key] || key;
}
